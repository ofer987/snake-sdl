#ifndef SNAKE_SDL_SNAKE_SDL_TYPES_H_
#define SNAKE_SDL_SNAKE_SDL_TYPES_H_

#include <stdlib.h>

enum MOVEMENTS { NOTHING, LEFT, UP, RIGHT, DOWN };

enum TILE_TYPES {
  AVAILABLE = 0b0,
  USED_BY_FOOD = 0b1,
  USED_BY_MESSAGE = 0b10,
  USED_BY_SNAKE_TAIL = 0b100,
  USED_BY_TOP_BORDER = 0b1000,
  USED_BY_TOP_RIGHT_BORDER = 0b10000,
  USED_BY_RIGHT_BORDER = 0b100000,
  USED_BY_BOTTOM_RIGHT_BORDER = 0b1000000,
  USED_BY_BOTTOM_BORDER = 0b10000000,
  USED_BY_BOTTOM_LEFT_BORDER = 0b100000000,
  USED_BY_LEFT_BORDER = 0b1000000000,
  USED_BY_TOP_LEFT_BORDER = 0b10000000000,
  USED_BY_SNAKE_HEAD = 0b100000000000,
  TOTAL = 13
};

typedef struct coordinates_struct {
  enum TILE_TYPES type;

  size_t x;
  size_t y;

  struct coordinates_struct* next;
} Coordinates;

#endif // SNAKE_SDL_SNAKE_SDL_TYPES_H_
